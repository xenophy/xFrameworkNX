require('NX');
var assert = require('assert');

// }}}
// {{{ NX Class Tests

module.exports = {

    // {{{ test sleep#standard

    'test sleep#standard': function() {

        var start = new Date;

        NX.sleep(1000);

        var end = new Date;
        var time = end.getTime() - start.getTime();

        assert.equal(Math.floor(time / 1000), 1);

    }

    // }}}

};

